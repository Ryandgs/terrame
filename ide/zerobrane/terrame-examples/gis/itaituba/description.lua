The file description.lua presents a general description of the data that is going
to be documented. You can use it to document the authors, licence of the data, and
other information (see all the available names [here](https://github.com/TerraME/terrame/wiki/Packages#Description)). To run this, instead of clicking in the green button, it is
necessary to click in the menu "Project" above and then "Build Documentation". The
documentation will be created in directory itaituba/doc. To see the final output,
click in "Project" and then "View Documentation". If the script "data.lua" does
not exist, or if you add new data to be documented, you can create/update
"data.lua" by running "Project" and then "Documentation Sketch".

