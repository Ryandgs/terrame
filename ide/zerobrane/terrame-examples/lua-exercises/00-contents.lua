# Lua Exercises

Here you will find some basic Lua exercises. The first part containts pieces of
code with problems. Some will stop with an error, while others will execute but
there is something wrong with the implementation. The objective is to find out
basic mistakes when creating Lua code. The second part is more algorithmic.
Pieces of code are given and the implementation should be written after them.

